#include<iostream>
using namespace std;

int main() {
 	
 	//变量的定义 
	//变量创建的语法：数据类型 变量名 = 变量初始值
	 
	 cout << "宝宝的出生日期" << endl;
	 
	 //创建变量 
	int year = 2004;
	cout << "year = " << year << endl;
 	
 	int mouth = 8;
 	cout << "mouth = " << mouth << endl;
 	
 	int day = 11;
 	cout << "day = " << day << endl;
 	
 	system("pause");
 	
 	return 0;
}
